type Section = "hero" | "skills" | "projects" | "contact";

const AnimatedBackground = () => {
  const { isLoading, bypassLoading } = usePreloader();
  const { theme } = useTheme();
  const isMobile = useMediaQuery("(max-width: 768px)");
  const splineContainer = useRef<HTMLDivElement>(null);
  const [splineApp, setSplineApp] = useState<Application>();

  if (isMobile) {
    return null;
  }

  const [activeSection, setActiveSection] = useState<Section>("hero");

  const keyboardStates = (section: Section) => {
    return STATES[section][isMobile ? "mobile" : "desktop"];
  };

  useEffect(() => {
    if (splineApp) {
      const kbd = splineApp.findObjectByName("keyboard");
      if (kbd) {
        gsap.to(kbd.scale, {
          ...keyboardStates(activeSection).scale,
          duration: 1,
        });
        gsap.to(kbd.position, {
          ...keyboardStates(activeSection).position,
          duration: 1,
        });
        gsap.to(kbd.rotation, {
          ...keyboardStates(activeSection).rotation,
          duration: 1,
        });
      }
    }
  }, [activeSection, splineApp, isMobile]);

  return (
    <>
      <Suspense fallback={<div>Carregando...</div>}>
        <Spline
          ref={splineContainer}
          onLoad={(app: Application) => {
            setSplineApp(app);
            bypassLoading();
          }}
          scene="/assets/skills-keyboard.spline"
        />
      </Suspense>
    </>
  );
};

export default AnimatedBackground;
